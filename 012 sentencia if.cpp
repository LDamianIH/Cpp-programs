/* Condicionales if - else */

#include<iostream>

using namespace std;

int main(){
	int numero, dato = 5;
	
	cout<<"Digite un numero cualquiera: "; cin>>numero;
	
	if(numero == dato){					//condicion de igualdad == condicion de desigualdad !=
		cout<<"El numero es 5";
	}
	else{
		cout<<"El numero es diferente de 5";
	}
	
	
	
	return 0;
}
